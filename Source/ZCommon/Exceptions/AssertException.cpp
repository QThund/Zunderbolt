AssertException::AssertException(const string_z &strCondition, const string_z &strMessage, const int nLineNumber, const string_z &strFileName) :
                                                                                                m_strCondition(strCondition),
                                                                                                m_strMessage(strMessage),
                                                                                                m_nLineNumber(nLineNumber),
                                                                                                m_strFileName(strFileName)
{
}


