string_z AssertException::GetCondition() const
{
    return m_strCondition;
}

string_z AssertException::GetMessage() const
{
    return m_strMessage;
}

int AssertException::GetLineNumber() const
{
    return m_nLineNumber;
}

string_z AssertException::GetFileName() const
{
    return m_strFileName;
}

