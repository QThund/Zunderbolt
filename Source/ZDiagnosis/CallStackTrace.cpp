void CallStackTrace::Push(const CallTrace &callTrace)
{
    m_arCallTraces.Add(callTrace);
}

void CallStackTrace::Pop()
{
    m_arCallTraces.Remove(m_arCallTraces.GetCount() - 1ULL);
}

CallTrace CallStackTrace::GetTrace(const puint_z uIndex) const
{
    return m_arCallTraces[uIndex];
}


